void ZOMBIE::setUpZombie(string name){
	this->name = name;
	level = (rand() % 20) + 1;
	strenght = (rand() % 10) + 1;
	shield = (rand() % 10) + 1;
	hp = 50 * level;
	specialAttack = 0.06 * level;
	cout << "\tZOMBIE: " << name << endl;
	cout << "\tLEVEL: " << level << "\t STRENGHT: " << strenght << "\t SHIELD: " << shield << endl;
	cout << "\tChance of Special Attack is: " << specialAttack * 100 << "%\n\n ";

}

int ZOMBIE::attack(){

	int damage = strenght * (level / 2);

	float specialShoot = rand() % 100;

	specialShoot = specialShoot / 100;

	if (specialShoot <= specialAttack)
	{
		cout << "\n~~~~~~~~~~~~~~~~~~~~~~ZOMBIES " << name << " has eaten a part of you~~~~~~~~~~~~~~~~~~~" << endl;
		return damage * 2;
	}
	else
		return damage;


}

int ZOMBIE::getHp(){
	return hp;
}


void ZOMBIE::takingDamage(int dmg){
	int damage = dmg - (shield * (level / 2));

	if (damage < 1)
		damage = 1;

	hp = hp - damage;

	cout << "\n\tZOMBIES " << name << " was attacked by you and have lost " << damage << " of points " << endl;
	cout << "\tCurrent HP of the ZOMBIES is " << hp << "!!!" << endl;


}

bool ZOMBIE::dead(){
	if (hp < 1)
		return true;
	else
		return false;
}


string ZOMBIE::getName(){
	return name;
}



